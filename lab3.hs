-- Завдання 1. Видалити повтори елементiв списку
-- a) без застосування вбудованих функцій
unique :: [Int] -> [Int]
unique xs = [x | (x,y) <- zip xs [0..], x `notElem` (take y xs)]
-- unique[1,3,3,2,1,2,5]
--[1,3,2,5]

