-- Завдання 2. Знайти найменше спiльне кратне двох чисел.
lcm :: Int -> Int -> Int
lcm x y =  abs ((x `quot` (gcd x y)) * y)
-- lcm 15 7
--105
-- lcm 18 24
--72
